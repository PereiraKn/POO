class Animal:
    def __init__(self, nome, espécie):
        self.nome = nome
        self.espécie = espécie
        
    def comer(self):
        return f"{self.nome} está comendo."
    
    def dormir(self):
        return f"{self.nome} está dormindo."
    
    def fazer_som(self):
        return f"{self.nome} está fazendo um som."
    

    
    
     
